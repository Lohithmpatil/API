package POJOforSerilizationandDesrilization;

public class friend {
	
	String name;
	String compoany;
	String addres;
	public friend(String name, String compoany, String addres) {

		this.name = name;
		this.compoany = compoany;
		this.addres = addres;
	}
	
	public friend() {
		
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCompoany() {
		return compoany;
	}
	public void setCompoany(String compoany) {
		this.compoany = compoany;
	}
	public String getAddres() {
		return addres;
	}
	public void setAddres(String addres) {
		this.addres = addres;
	}
	
	

}
